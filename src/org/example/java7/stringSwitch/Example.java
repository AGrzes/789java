package org.example.java7.stringSwitch;

public class Example {
    public static void main(String[] args) {
        String var = "A";
        switch (var) {
        case "B":
            System.out.println("B selected");
            break;
        case "C":
            System.out.println("C selected");
            break;
        case "A":
            System.out.println("A selected");
            break;
        }
    }
}
